        <p>Meditation is a method for calming the mind and achieving a state of consciousness that differs from normal waking consciousness. 
            It’s a way of understanding all of our degrees of self-awareness and eventually experiencing the core of consciousness inside. 
            If you want to practice meditation and get in the habit of mindfulness, here are the things you should know about meditation for beginners.</p><br>
        
        <h2 class="h2_blog">What Happens During Meditation?</h2><br>
        <p>The mind is calm, relaxed, and inwardly concentrated during meditation. 
            You are fully awake and alert while you meditate, but your mind is not based on the outside world or the activities that are taking place around you. 
            Meditation necessitates an inner state of stillness and focuses on the mind to become silent. Meditation deepens when the mind is still and no longer distracts you.</p><br>
        <p>Meditation for Beginners: How to Do It</p>
        <ul class="ul-blog">
            <li>Sit or lie down in a comfortable position. You may want to consider purchasing a meditation chair or cushion.</li>
            <li>Close your eyes for a moment. </li>
            <li>Don’t attempt to regulate your breathing; just let it happen naturally.</li>
            <li>Concentrate on the breathing and body movement as you inhale and exhale. </li>
            <li>Try to focus back to your breathing if your mind wanders.</li>
        </ul><br>
            <p>So, don’t expect to have deep states of meditation in the very first week. It takes some time to feel the change. While few may have a good experience the very first time. The problem with expectation is that when you don’t see results within a week, you slowly lose interest in the practice. 
                Remember:  Gradual Steady daily Practice is what is required.</p>
